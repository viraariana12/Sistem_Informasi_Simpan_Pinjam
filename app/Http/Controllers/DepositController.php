     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
    
        $form_data = array(
            'kd_simpanan'           =>  $request->kd_simpanan,
            'saldo'                 =>  $request->jml_simpan,
            'id_anggota'            =>  $request->id_anggota,
            'id_transaksi'          =>  $request->id_transaksi,
        );
        Deposit::create($form_data);
        return redirect('/admin/validasi/transaction')->with('status', 'Saldo Berhasil di tambah');
    }

